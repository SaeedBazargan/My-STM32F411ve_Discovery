// <---- ------------ Accelometer Initialize ------------ ---->
void Accel_LSM303DL_Init(void)
{
	LSM303DL.Accel_PWR 				= LSM303DLHC_NORMAL_MODE;
	LSM303DL.Accel_DataRate 		= LSM303DLHC_ODR_1344_HZ;
	LSM303DL.Accel_Axes 			= LSM303DLHC_AXES_ENABLE;
	LSM303DL.Accel_HighResEn 		= LSM303DLHC_HR_DISABLE;
	LSM303DL.Accel_Scale 			= LSM303DLHC_FULLSCALE_16G;
	LSM303DL.Accel_FilterMode 		= LSM303DLHC_HPM_NORMAL_MODE_RES;
	LSM303DL.Accel_FilterCutFreq 	= LSM303DLHC_HPFCF_8;

	isInitialized = LSM303DL_Init(&hi2c1, &LSM303DL);
	if(isInitialized != LSM303DL_RESULT_OK)
		Error_Handler();
}

